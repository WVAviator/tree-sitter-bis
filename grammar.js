/// <reference types="tree-sitter-cli/dsl" />
// @ts-check

export default grammar({
  name: "bis",
  extras: (_) => [/\s/],

  rules: {
    source_file: ($) => repeat($._line),
    _line: ($) => choice($.statement),
    statement: ($) =>
      seq(
        $.stmt_prefix,
        repeat($._stmt_contents),
        $.stmt_terminator,
        $.comment,
      ),
    stmt_prefix: (_) => "@",
    _stmt_contents: ($) => choice($._ldv),
    stmt_terminator: (_) => ".",
    separator: ($) => seq(/\\/, $.comment),
    comment: (_) => prec(-1, seq(/[^\n]*/, /\n/)),

    // Variables (See defining_variables.md)

    named_variable: ($) => token(seq("<", /[^>]{1,12}/, ">")),
    global_variable: ($) => token(seq("<*", /[^>]{1,12}/, ">")),
    env_variable: ($) => token(seq("<$", /[^>]{1,12}/, ">")),
    numbered_variable: ($) => token(seq(/[Vv]/, /\d{1,3}/)),

    type: ($) => token(seq(/[afhiosAFHIOS]/, /\d{1,3}/, optional(/\.\d{1,2}/))),
    substring: ($) => seq("(", /\d+/, "-", /\d+/, ")"),

    _variable: ($) =>
      seq(
        choice(
          $.named_variable,
          $.global_variable,
          $.env_variable,
          $.numbered_variable,
        ),
        optional(choice($.type, $.substring)),
      ),

    // Literals

    string: ($) =>
      token(choice(seq("'", /[^']*/, "'"), seq(/"/, /[^"]*/, /"/))),
    number: ($) =>
      token(choice(/\d+/, /\d*\.\d+/, /\d+[Ee]\-?\d+/, /\d*\.\d+[Ee]\-?\d+/)),
    character: ($) => token(seq('(', /./, ')')),
    _value_definition: ($) => choice($.string, $.number, $._variable),

    // Statement Parts

    call: ($) => token(choice(...ALL_CALLS.map(ci))),
    options: $ => token(/[A-Za-z]+/),

    // LDV

    _ldv: ($) => choice($._ldv_fmt_1, $._ldv_fmt_2, $._ldv_fmt_3),

    _ldv_call: $ => alias(/[Ll][Dd][Vv]/, $.call),
    _assignment: $ => seq($._variable, '=', $._value_definition),
    _assignment_delimited: $ => seq($._assignment, ',', $.number, optional($.character)),

    _ldv_fmt_1: $ => seq(
      $._ldv_call,
      optional(seq(',', $.options)),
      $._assignment,
      repeat(seq(',', $._assignment))
    ),
    _ldv_fmt_2: $ => seq(
      $._ldv_call,
      seq(',', $.options),
      $._variable,
      repeat(seq(',', $._variable)),
    ),
    _ldv_fmt_3: $ => seq(
      $._ldv_call,
      seq(',', alias(/[A-Za-z]*[Qq][A-Za-z]*/, $.options)),
      $._assignment_delimited,
      repeat(seq(',', $._assignment_delimited)),
    ),
  },
});
